 function saveFormData() {
            // Get the selected values from checkboxes
            var subjects = [];
            var checkboxes = document.getElementsByName("subjects");
            
            for (var i = 0; i < checkboxes.length; i++) {
                if (checkboxes[i].checked) {
                    subjects.push(checkboxes[i].value);
                }
            }
            
            // Set cookie with the selected values
            document.cookie = "subjects=" + subjects.join(",") + "; expires=Thu, 18 Apr 2024 12:00:00 UTC; path=/";
            
            alert("Form data saved!");
        }
        
        function retrieveFormData() {
            // Retrieve cookie and display the selected values
            var subjects = getCookie("subjects");
            if (subjects) {
                var subjectsArray = subjects.split(",");
                for (var i = 0; i < subjectsArray.length; i++) {
                    document.getElementById("subject_" + subjectsArray[i]).checked = true;
                }
            }
        }
        
        function getCookie(cookieName) {
            var name = cookieName + "=";
            var decodedCookie = decodeURIComponent(document.cookie);
            var cookieArray = decodedCookie.split(';');
            
            for (var i = 0; i < cookieArray.length; i++) {
                var cookie = cookieArray[i];
                while (cookie.charAt(0) == ' ') {
                    cookie = cookie.substring(1);
                }
                if (cookie.indexOf(name) == 0) {
                    return cookie.substring(name.length, cookie.length);
                }
            }
            return "";
}

